# ── Model ──────────────────────────────────────────────────────────────────────
def train_model(csv_path="movies.csv"):
    """Load data and compute TF-IDF cosine similarity matrix."""
    df = pd.read_csv(csv_path).dropna(subset=["title", "genres"])
    df["title"] = df["title"].str.strip()
    df["genres"] = df["genres"].str.strip()
    df = df.drop_duplicates(subset="title").reset_index(drop=True)

    # TF-IDF weighs rarer genre tokens higher → better discrimination than raw counts
    tfidf = TfidfVectorizer()
    vectors = tfidf.fit_transform(df["genres"])
    similarity = cosine_similarity(vectors)

    return df, similarity


# ── Fuzzy title lookup ─────────────────────────────────────────────────────────
def find_closest_title(query, titles):
    """
    Case-insensitive substring match first; falls back to
    character-overlap scoring so typos still find results.
    """
    query_lower = query.lower().strip()
    title_lower = [t.lower() for t in titles]

    # 1. Exact match
    if query_lower in title_lower:
        return titles[title_lower.index(query_lower)]

    # 2. Substring match
    matches = [t for t, tl in zip(titles, title_lower) if query_lower in tl]
    if matches:
        return matches[0]

    # 3. Character-overlap fallback (handles minor typos)
    def overlap(a, b):
        return len(set(a) & set(b)) / max(len(set(a)), len(set(b)), 1)

    scored = [(t, overlap(query_lower, tl)) for t, tl in zip(titles, title_lower)]
    best_title, best_score = max(scored, key=lambda x: x[1])
    return best_title if best_score >= 0.5 else None


# ── Recommendation engine ──────────────────────────────────────────────────────
def recommend(movie_query, df, similarity, top_n=5):
    """
    Return top-N recommendations with similarity scores.
    Handles case differences and partial/fuzzy titles.
    """
    matched = find_closest_title(movie_query, df["title"].tolist())

    if matched is None:
        print(f"\n❌ '{movie_query}' not found. Try a different title.")
        return

    if matched.lower() != movie_query.lower():
        print(f"\n🔍 Showing results for: '{matched}' (closest match to '{movie_query}')")

    idx = df[df["title"] == matched].index[0]
    source_genres = df.iloc[idx]["genres"]

    # Pair every movie with its similarity score, exclude the query itself
    scored = [
        (df.iloc[i]["title"], df.iloc[i]["genres"], round(sim, 4))
        for i, sim in enumerate(similarity[idx])
        if i != idx
    ]
    scored.sort(key=lambda x: x[2], reverse=True)
    top = scored[:top_n]

    # ── Pretty output ──────────────────────────────────────────────────────────
    print(f"\n{'─'*50}")
    print(f"  🎬  Because you liked: {matched}")
    print(f"       Genres: {source_genres}")
    print(f"{'─'*50}")

    for rank, (title, genres, score) in enumerate(top, 1):
        bar_len = int(score * 20)
        bar = "█" * bar_len + "░" * (20 - bar_len)
        pct = int(score * 100)
        print(f"  {rank}. {title:<35} [{bar}] {pct:>3}%")
        print(f"     Genres: {genres}")
    print(f"{'─'*50}\n")


