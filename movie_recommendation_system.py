def main():
    df, similarity = train_model()
    print(f"\n🎥 Movie Recommender — {len(df)} titles loaded")
    print("   Type a movie name, or 'list' to browse, or 'quit' to exit.\n")

    while True:
        query = input("Enter movie name: ").strip()

        if not query:
            continue
        if query.lower() == "quit":
            print("Bye! 🎬")
            break
        if query.lower() == "list":
            cols = 3
            titles = df["title"].tolist()
            for i in range(0, len(titles), cols):
                row = titles[i:i+cols]
                print("  " + "   ".join(f"{t:<28}" for t in row))
            print()
            continue

        try:
            n_input = input("How many recommendations? [default 5]: ").strip()
            top_n = int(n_input) if n_input.isdigit() and int(n_input) > 0 else 5
        except Exception:
            top_n = 5

        recommend(query, df, similarity, top_n=top_n)


if __name__ == "__main__":
    main()